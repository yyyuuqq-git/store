        document.getElementById('detail-tip').textContent = "근처 현지인 노포 맛집 투어를 곁들이면 훨씬 알차고 맛있는 자유여행 코스가 완성됩니다. 방문 전 날씨 예보를 한 번 확인하시는 것을 권장합니다.";
        document.getElementById('detail-time').textContent = "오전 10:00 ~ 오후 8:00 (주/야간 모두 조망 훌륭)";
        document.getElementById('detail-fee').textContent = "무료 (체험 및 개별 소비 발생 가능)";
